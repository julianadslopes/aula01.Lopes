# Variáveis são pequenos espaços de memória, utilizados para armazenar e
# manipular dados. Em Python, os tipos de dados básicos são tipo inteiro
# (armazena números inteiros), tipo float (armazena números em formato
# decimal), tipo string (armazena um conjunto de caracteres) e tipo Bool,
# que armazena True ou False.
# Cada variável pode armazenar apenas um tipo de dado a cada instante.
# Em Python, diferentemente de outras linguagens de programação, não é preciso
# declarar de que tipo será cada variável no início do programa.


#  Tipos de Variáveis:
# int - Número inteiros
# float - Números decimais
# String - Cadeia de caracteres (Valores entre aspas)
# bool - Valores booleanos: True ou False (sempre com a primeira
# letra maiúscula)

# Variáveis
# Quando se faz uma atribuição de valor, automaticamente, a variável se
# torna do chip o do valor armazenado, Como apresentado nos exemplos a seguir.


# Exemplos


a = 29
# A variável se torna uma variável do tipo inteira.


b = 1.8
# A variável b se torna uma variável do tipo float.


c = 'Ao infinito e além!!!'
# A variável se torna uma variável do tipo string.


