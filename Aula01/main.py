# Operadores Aritméticos
# + Operador de adição
# - Operador de subtração
# * Operador de Multiplicação
# / Operador de Divisão
# // Operador de Divisão Inteira
# % Operador Resto da Divisão (MOD)

# Operação com variáveis
n1 = 5
n2 = 2

# Operações entre N1 e N2
print('Operações entre', n1, 'e', n2)
print('Soma', n1 + n2)
print('Subtralção', n1 - n2)
print('Multiplicação', n1 * n2)
print('Divisão', n1 / n2)
print('Divisão Inteira', n1 // n2)
print('O quadrado de', n1, 'é', n1 ** n2)
print('O resto da divisão entre', n1, 'e', n2, 'resulta em', n1 % n2)

